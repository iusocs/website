				Cognitive Science students at Indiana University. We meet weekly for pizza and discussion, in addition to organizing special events, including:

<ul>
<li>Professor Dinners</li>
<li>Movie Nights</li>
<li>Informal Q&A Sessions with Visiting Scholars</li>
<li>TED Parties</li>
</ul>
