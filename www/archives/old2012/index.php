                <p>The <a href="mucsc/index.html">Midwest Undergraduate Cognitive Science Conference (MUCSC)</a> is an annual undergraduate 
conference organized and sponsored by SOCS. Currently in its 4th year, the conference has built a reputation throughout the region and has attracted 
young scholars from several midwestern universities. Previous keynote speakers include Douglas Hofstadter,  Larry Yaeger, and Edward Castroneves.
This year's conference is scheduled for April 14, 2013 and the 
keynote 
will be given by Melanie Mitchell, Professor of Computer Science at Portland State University.  For more 
information, see the <a href="mucsc/index.html">MUCSC site</a>.</p>
